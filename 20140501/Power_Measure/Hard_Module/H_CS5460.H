#ifndef __H_CS5460_H__
#define __H_CS5460_H__

extern long  CS5460_RegBuf[18];  // CS5460的寄存器值

/*=======================================================================
函数名称：CS5460_Send
函数功能: CS5460 SPI 发送数据的函数
输入参数：要发送的数据
输出参数：返回当前SDO输出的数据
其它说明：无
=======================================================================*/
extern unsigned char CS5460_Send( unsigned char Send_Data );

/*=======================================================================
函数名称：CS5460_SendCmd
函数功能: CS5460 SPI 发送命令的函数
输入参数：要发送的命令
输出参数：无
其它说明：无
=======================================================================*/
extern void CS5460_SendCmd( unsigned char Send_Data );

/*=======================================================================
函数名称：CS5460_ReadReg
函数功能：读取CS5460的某一项寄存器的函数
输入参数：要获取的寄存器
输出参数：当前寄存器的内容
其它说明：无
=======================================================================*/
extern long CS5460_ReadReg( unsigned char Reg_Address );

/*=======================================================================
函数名称：CS5460_ReadAllReg
函数功能：顺序读取CS5460的n个寄存器的函数
输入参数：要读取的寄存器起始地址和要读取的寄存器数量
输出参数：无
其它说明：读出的结果放入到了long  CS5460_RegBuf[18]寄存器值缓冲区中
=======================================================================*/
//extern void CS5460_ReadAllReg( unsigned char RegStart_Address, unsigned char Read_Number );

/*=======================================================================
函数名称：CS5460_WriteReg
函数功能：写CS5460的某一项寄存器的函数
输入参数：要写的寄存器地址,要写入的数据0、数据1，数据2
输出参数：无
其它说明：无
=======================================================================*/
extern void CS5460_WriteReg( unsigned char Reg_Address, unsigned char Reg_Data0, unsigned char Reg_Data1, unsigned char Reg_Data2 );

#endif